L_3:
	daddu r20,r5,$zero
	dadd r3, r2, $zero	;j = 0 	(start new loop for j)
	daddi r22, r2, 400

	;get A[i][k]
	daddu r10, r2, r4	;sum = (i*size*8) + (k*8)
	ld r11, A(r10)		;A[i*size + k]

